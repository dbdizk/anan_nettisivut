import fs from "node:fs/promises";
import path from "node:path";

const PROJECT_ROOT = process.cwd();
const AUDIO_DIR = path.join(PROJECT_ROOT, "public", "audio");
const OUTPUT_FILE = path.join(PROJECT_ROOT, "data", "audio-tracks.generated.ts");

/** @param {string} dir */
async function walk(dir) {
  /** @type {string[]} */
  const files = [];
  const entries = await fs.readdir(dir, { withFileTypes: true });
  for (const entry of entries) {
    const abs = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await walk(abs)));
    } else if (entry.isFile()) {
      files.push(abs);
    }
  }
  return files;
}

function toPosix(p) {
  return p.split(path.sep).join("/");
}

function toUrlPath(posixPath) {
  return posixPath.split("/").map(encodeURIComponent).join("/");
}

function humanize(part) {
  return part.replace(/[-_]+/g, " ").trim();
}

function parseArtistSong(filenameNoExt) {
  const parts = filenameNoExt.split("_");
  if (parts.length >= 2) {
    const artist = humanize(parts[0] ?? "");
    const song = humanize(parts.slice(1).join("_"));
    return { artist, title: song || artist || filenameNoExt };
  }

  const title = humanize(filenameNoExt);
  return { artist: "", title: title || filenameNoExt };
}

async function main() {
  let audioFilesAbs;
  try {
    audioFilesAbs = await walk(AUDIO_DIR);
  } catch (err) {
    console.error(`[generate-audio-tracks] Could not read ${AUDIO_DIR}`);
    throw err;
  }

  const preferredOrder = ["KYOTO", "TEMPORARY", "REMOTE", "SPACETIME", "FASTLANE"];
  const tracks = audioFilesAbs
    .filter((p) => /\.(mp3|m4a|aac|wav|ogg|flac)$/i.test(p))
    .map((abs) => {
      const rel = path.relative(AUDIO_DIR, abs);
      const relPosix = toPosix(rel);
      const urlPath = toUrlPath(relPosix);

      const file = path.basename(relPosix);
      const base = file.replace(/\.[^.]+$/, "");
      const meta = parseArtistSong(base);

      return {
        relPosix,
        src: `/audio/${urlPath}`,
        title: meta.title,
        artist: meta.artist,
      };
    })
    .filter((t) => preferredOrder.includes(t.title))
    .sort((a, b) => preferredOrder.indexOf(a.title) - preferredOrder.indexOf(b.title));

  const lines = [];
  lines.push("// AUTO-GENERATED FILE. DO NOT EDIT.");
  lines.push("// Generated by scripts/generate-audio-tracks.mjs");
  lines.push("");
  lines.push("export const audioTracks = [");
  for (const t of tracks) {
    lines.push("  {");
    lines.push(`    title: ${JSON.stringify(t.title)},`);
    lines.push(`    artist: ${JSON.stringify(t.artist)},`);
    lines.push(`    src: ${JSON.stringify(t.src)},`);
    lines.push("  },");
  }
  lines.push("] as const;");
  lines.push("");

  await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
  await fs.writeFile(OUTPUT_FILE, lines.join("\n"), "utf8");

  console.log(
    `[generate-audio-tracks] Wrote ${tracks.length} tracks to ${path.relative(PROJECT_ROOT, OUTPUT_FILE)}`
  );
}

main().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});
